// O loop while executa um bloco de código enquanto uma condição for verdadeira
// A condição é verificada antes de cada iteração
// Exemplo: Imprimir os números de 1 a 5
// O loop while é útil quando não sabemos quantas iterações serão necessárias

// Declara uma variável contador iniciando em 1
let contador = 1;

// Enquanto contador for menor ou igual a 5
while (contador <= 5) {
    console.log(contador);
    contador++;
}
