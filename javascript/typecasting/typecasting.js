
let variavel = true;
console.log(typeof variavel); // saída: boolean

variavel = String(variavel);
console.log(typeof variavel); // saída: string

let str = '123';
console.log(typeof str); // saída: string

let num = Number(str);
console.log(typeof num); // saída: number

