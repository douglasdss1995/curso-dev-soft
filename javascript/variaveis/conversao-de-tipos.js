//Conversão de tipos - Type Casting
let variavel = true;
console.log(typeof variavel); //boolean

variavel = String(variavel);
console.log(typeof variavel); //string

let str = "123";
console.log(typeof str); //string

let num = Number(str);
console.log(typeof num); //number

let textoDecimal = "3.14";
let numeroDecimal = parseFloat(textoDecimal);//Converte  para numeros decimais
console.log(numeroDecimal); //Saida 3.14


